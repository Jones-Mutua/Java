package io.baraka._11_Functions;

public class BuiltInFunctions {
    public static void main(String[] args) {
        /*
         There are many common functions that developers want to use
         but do not want to define
         Java has defined some functions for us
         We can just call a built-in function because its already defined
                 System.out.println();
         */
        String name = "Barak";
        System.out.println(name.toLowerCase());

        /*
        Using Math for robust arithmetical operations

         */
        System.out.println(Math.pow(2, 5));;
    }
}
